/**
 * List Interface
 * 
 * @author kendr
 */
public interface List<E> {
    /**
     * Returns the number of elements in the list.
     * 
     * @return number of elements in the list
     */
    int size();

    /**
     * Tests whether the list is empty.
     * 
     * @return true if the list is empty, false otherwise
     */
    boolean isEmpty();

}